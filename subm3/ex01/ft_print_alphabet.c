#include <unistd.h>

void	ft_print_alphabet(char c)
{
	write(1, &c, 1);
}

void	komozi(void)
{
	char	a;

	a = 'a';
	while (a <= 'z')
	{
		ft_print_alphabet(a);
		a++;
	}
}
